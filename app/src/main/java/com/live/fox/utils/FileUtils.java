    public static String getTotalCacheSize(Context context) throws Exception {
        //得到 data/data/<application package>/cache目录下的数据大小
        long cacheSize = getDirLength(context.getCacheDir());
//        LogUtils.e("内部存储位置："+context.getCacheDir() +", 大小:"+getFormatSize(cacheSize));
        if (Environment.getExternalStorageState().equals(Environment.MEDIA_MOUNTED)) {
            //得到 SDCard/Android/data/你的应用包名/cache目录下的数据大小
            cacheSize += getDirLength(context.getExternalCacheDir());
//            LogUtils.e("外部存储位置："+context.getExternalCacheDir() +", 大小:" + getFormatSize(getDirLength(context.getExternalCacheDir())));
        }
        return getFormatSize(cacheSize);
    }

